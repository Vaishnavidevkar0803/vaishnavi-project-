package loops;

public class Table_Of_10 {

	public static void main(String[] args) 
	{
		int i=10;
		for(int j=1;j<=10;j++)
		System.out.println(i+"*"+j+"="+ i*j);
		System.out.println("------------------------");
		
		
		int i1=10;
		
		for(int j1=10;j1>=1;j1--)
	    System.out.println(i1 + "*" + j1 + "="+ i1*j1);
		
	}
	//reverse table of 10
	
	
	
	
}
